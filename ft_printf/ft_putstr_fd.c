#include "ft_printf.h"

int	ft_putstr_fd(char *s, int fd)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
	{
		j += write(fd, &s[i], 1);
		i++;
	}
	return (j);
}
